/** Convenience selectors used by the orchestrator. */
export function printedFields(spec: DocTypeSpec): string[] {
  return spec.fields.filter((f) => f.cls === 'printed').map((f) => f.key)
}
export function openNameFields(spec: DocTypeSpec): string[] {
  return spec.fields.filter((f) => f.cls === 'open_name').map((f) => f.key)
}
