/**
 * engine/docTypes.ts — the canonical FIELD STRUCTURE per Ukrainian document
 * type. This is the "правильная структура" the whole engine reads from:
 * which fields exist, their Ukrainian label (for HTR line→field mapping),
 * their kind (drives KMU-55 / gazetteer / date normalization), and their
 * CLASS (drives the consensus accept/confirm rule):
 *
 *   - 'open_name' : surnames, given names, free authority text. NEVER
 *                   auto-accepted even when readers agree (shared/systematic
 *                   misread guard — the "Тимофевич" case). Shown as suggestion.
 *   - 'closed'    : verifiable against a closed set / format (dates, oblast,
 *                   sex, gazetteer city, generated patronymic). Auto-accept on
 *                   agreement.
 *   - 'printed'   : machine-printed identifiers (series/number). A single
 *                   confident read is acceptable (with confirm).
 */

export type FieldKind = 'name' | 'date' | 'place_city' | 'place_oblast' | 'number' | 'sex' | 'text'
export type FieldClass = 'open_name' | 'closed' | 'printed'

export interface DocFieldSpec {
  key: string
  label_uk: string[]   // labels as they appear on the document (for HTR mapping)
  kind: FieldKind
  cls: FieldClass
  handwritten: boolean
}

export interface DocTypeSpec {
  id: string
  title_en: string
  fields: DocFieldSpec[]
}

const N = (key: string, label_uk: string[], cls: FieldClass, handwritten = true): DocFieldSpec => ({ key, label_uk, kind: 'name', cls, handwritten })
const D = (key: string, label_uk: string[], handwritten = true): DocFieldSpec => ({ key, label_uk, kind: 'date', cls: 'closed', handwritten })
const NUM = (key: string, label_uk: string[], printed = true): DocFieldSpec => ({ key, label_uk, kind: 'number', cls: printed ? 'printed' : 'closed', handwritten: !printed })

